package week2d1.Depot;

public class Size {
	private int length;
	private int heigth;
	private int width;
	private int weigth;
	
	public Size(int length, int heigth, int width, int weigth) {
		super();
		this.length = length;
		this.heigth = heigth;
		this.width = width;
		this.weigth = weigth;
	}

	public int getLength() {
		return length;
	}
	public void setLength(int length) {
		this.length = length;
	}
	public int getHeigth() {
		return heigth;
	}
	public void setHeigth(int heigth) {
		this.heigth = heigth;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getWeigth() {
		return weigth;
	}
	public void setWeigth(int weigth) {
		this.weigth = weigth;
	}
	
}
